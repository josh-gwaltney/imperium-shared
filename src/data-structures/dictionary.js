////////////////////////////////////////////////////////////////////////////////
// Class
////////////////////////////////////////////////////////////////////////////////
class Dictionary {
  constructor(){
    this._data = {};
  }

  add(key, value){
    this._data[key] = value;
  }

  delete(key){
    delete this._data[key];
  }

  find(key){
    return this._data[key];
  }

  clear(){
    this._data = {};
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default Dictionary;