class LogService{

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////
  _log;

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  constructor(){
    this._log = [];
  }

  ////////////////////////////////////////////////////////////////////////////////
  // Public Methods
  ////////////////////////////////////////////////////////////////////////////////
  success(msg){
    this._write('Success: ' + msg);
  }

  warning(msg){
    this._write('Warning: ' + msg);
  }

  debug(msg){
    this._write('Debug: ' + msg);
  }

  error(msg){
    this._write('Error: ' + msg);
  }

  clear(){
      this._log = [];
  }

  ////////////////////////////////////////////////////////////////////////////////
  // Private Methods
  ////////////////////////////////////////////////////////////////////////////////
  _write(msg){
      this._log.push(msg);
  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default Logger;