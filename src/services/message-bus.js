class MessageBus {

  //////////////////////////////////////////////////////////////////////////////
  // Private Properties
  //////////////////////////////////////////////////////////////////////////////

  //////////////////////////////////////////////////////////////////////////////
  // Public Properties
  //////////////////////////////////////////////////////////////////////////////

  constructor(){
    this._subscribers = {};
  }

  ////////////////////////////////////////////////////////////////////////////////
  // Public Methods
  ////////////////////////////////////////////////////////////////////////////////
  subscribe(msg, sub){
    if(!(msg in this._subscribers)){
      this._subscribers[msg] = [];
    }

    let subs = this._subscribers[msg];
    let idx = subs.indexOf(sub);

    if(idx === -1) {
      subs.push(sub);
    }
  }

  unsubscribe(msg, sub){
    if(msg in this._subscribers){
      let subs = this._subscribers[msg];
      let idx = subs.indexOf(sub);
      if(idx > -1){
        subs.slice(idx, 0);
      }
    }
  }

  publish(msg){
    let subs = this._subscribers[msg.subject];
    subs.forEach((o) => {
      o(msg.body);
    });
  }

  ////////////////////////////////////////////////////////////////////////////////
  // Private Methods
  ////////////////////////////////////////////////////////////////////////////////
  _hasSubscriber(msg, sub){

  }
}

////////////////////////////////////////////////////////////////////////////////
// Exports
////////////////////////////////////////////////////////////////////////////////
export default MessageBus;